def adam(
    params: List[Tensor],
    grads: List[Tensor],
    exp_avgs: List[Tensor],
    exp_avg_sqs: List[Tensor],
    max_exp_avg_sqs: List[Tensor],
    state_steps: List[Tensor],
    weight_decay_sample_size: int,
    # kwonly args with defaults are not supported by functions compiled with torchscript issue #70627
    # setting this as kwarg for now as functional API is compiled by torch/distributed/optim
    foreach: Optional[bool] = None,
    capturable: bool = False,
    differentiable: bool = False,
    fused: Optional[bool] = None,
    grad_scale: Optional[Tensor] = None,
    found_inf: Optional[Tensor] = None,
    has_complex: bool = False,
    *,
    amsgrad: bool,
    beta1: float,
    beta2: float,
    lr: Union[float, Tensor],
    weight_decay: float,
    eps: float,
    maximize: bool,
):
    r"""Functional API that performs Adam algorithm computation.

    See :class:`~torch.optim.Adam` for details.
    """
    # Respect when the user inputs False/True for foreach or fused. We only want to change
    # the default when neither have been user-specified. Note that we default to foreach
    # and pass False to use_fused. This is not a mistake--we want to give the fused impl
    # bake-in time before making it the default, even if it is typically faster.
    if fused is None and foreach is None:
        _, foreach = _default_to_fused_or_foreach(
            params, differentiable, use_fused=False
        )
        # Do not flip on foreach for the unsupported case where lr is a Tensor and capturable=False.
        if foreach and isinstance(lr, Tensor) and not capturable:
            foreach = False
    if fused is None:
        fused = False
    if foreach is None:
        foreach = False

    func = _single_tensor_adam

    func(
        params,
        grads,
        exp_avgs,
        exp_avg_sqs,
        max_exp_avg_sqs,
        state_steps,
        weight_decay_sample_size=weight_decay_sample_size,
        amsgrad=amsgrad,
        has_complex=has_complex,
        beta1=beta1,
        beta2=beta2,
        lr=lr,
        weight_decay=weight_decay,
        eps=eps,
        maximize=maximize,
        capturable=capturable,
        differentiable=differentiable,
        grad_scale=grad_scale,
        found_inf=found_inf,
    )


def _single_tensor_adam(
    params: List[Tensor],
    grads: List[Tensor],
    exp_avgs: List[Tensor],
    exp_avg_sqs: List[Tensor],
    max_exp_avg_sqs: List[Tensor],
    state_steps: List[Tensor],
    grad_scale: Optional[Tensor],
    found_inf: Optional[Tensor],
    weight_decay_sample_size: int,
    *,
    amsgrad: bool,
    has_complex: bool,
    beta1: float,
    beta2: float,
    lr: Union[float, Tensor],
    weight_decay: float,
    eps: float,
    maximize: bool,
    capturable: bool,
    differentiable: bool,
):

    assert grad_scale is None and found_inf is None

    if torch.jit.is_scripting():
        # this assert is due to JIT being dumb and not realizing that the ops below
        # have overloads to handle both float and Tensor lrs, so we just assert it's
        # a float since most people using JIT are using floats
        assert isinstance(lr, float)

    for i, param in enumerate(params):
        grad = grads[i] if not maximize else -grads[i]
        exp_avg = exp_avgs[i]
        exp_avg_sq = exp_avg_sqs[i]
        step_t = state_steps[i]

        # update step
        step_t += 1

        # get indices of non-zero gradients
        grad_indices = grad.indices()
        grad_values = grad.values()
        # if embedding table is smaller than weight_decay_sample_size
        # we do regular weight decay over the whole embedding table
        weight_decay_sample_size = min(weight_decay_sample_size, grad.size(0))
        # sample weight_decay_sample_size indices to apply weight decay
        i = torch.cat(
            [
                grad_indices,
                torch.randperm(grad.size(0))[:weight_decay_sample_size].unsqueeze(0),
            ],
            dim=-1,
        )
        v = torch.cat(
            [grad_values, torch.zeros(weight_decay_sample_size, grad_values.size(1))],
            dim=0,
        )
        # doing coalesce again to make sure that the indices are unique
        grad = torch.sparse_coo_tensor(i, v, grad.shape).coalesce()
        grad_indices = grad.indices().squeeze()
        grad_values = grad.values()

        if weight_decay != 0:
            grad_values = grad_values.add(param[grad_indices], alpha=weight_decay)

        # Decay the first and second moment running average coefficient
        exp_avg[grad_indices] = exp_avg[grad_indices].lerp_(grad_values, 1 - beta1)
        exp_avg_sq[grad_indices] = (
            exp_avg_sq[grad_indices]
            .mul_(beta2)
            .addcmul_(grad_values, grad_values.conj(), value=1 - beta2)
        )

        step = _get_value(step_t)

        bias_correction1 = 1 - beta1**step
        bias_correction2 = 1 - beta2**step

        step_size = lr / bias_correction1

        bias_correction2_sqrt = _dispatch_sqrt(bias_correction2)

        denom = (exp_avg_sq[grad_indices].sqrt() / bias_correction2_sqrt).add_(eps)

        param[grad_indices] = param[grad_indices].addcdiv_(
            exp_avg[grad_indices], denom, value=-step_size
        )
